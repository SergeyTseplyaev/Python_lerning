# –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã–≤–æ–¥–∏—Ç –≤—Å–µ –≤–≤–µ–¥–µ–Ω–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏, –≤ –∫–æ—Ç–æ—Ä—ã—Ö –≤—Å—Ç—Ä–µ—á–∞—é—Ç—Å—è –≤—Å–µ –ø–æ–∏—Å–∫–æ–≤—ã–µ –∑–∞–ø—Ä–æ—Å—ã.

# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ nn ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫, –∑–∞—Ç–µ–º —Å–∞–º–∏ —Å—Ç—Ä–æ–∫–∏ –≤ —É–∫–∞–∑–∞–Ω–Ω–æ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–µ, –∑–∞—Ç–µ–º —á–∏—Å–ª–æ kk, –∑–∞—Ç–µ–º —Å–∞–º–∏ –ø–æ–∏—Å–∫–æ–≤—ã–µ –∑–∞–ø—Ä–æ—Å—ã.

# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –≤—Å–µ –≤–≤–µ–¥–µ–Ω–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏, –≤ –∫–æ—Ç–æ—Ä—ã—Ö –≤—Å—Ç—Ä–µ—á–∞—é—Ç—Å—è –≤—Å–µ –ø–æ–∏—Å–∫–æ–≤—ã–µ –∑–∞–ø—Ä–æ—Å—ã.

# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ü–æ–∏—Å–∫ –Ω–µ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å —á—É–≤—Å—Ç–≤–∏—Ç–µ–ª–µ–Ω –∫ —Ä–µ–≥–∏—Å—Ç—Ä—É —Å–∏–º–≤–æ–ª–æ–≤.


n = int(input())

lst = []   # —Å–ø–∏—Å–æ–∫ –¥–ª—è —Å—Ç—Ä–æ–∫, –ø–æ –∫–æ—Ç–æ—Ä—ã–º –±—É–¥–µ–º –∏—Å–∫–∞—Ç—å
zaprosi = []   # —Å–ø–∏—Å–æ–∫, –≤ –∫–æ—Ç–æ—Ä–æ–º —Ö—Ä–∞–Ω–∏–º –ø–æ–∏—Å–∫–æ–≤—ã–µ –∑–∞–ø—Ä–æ—Å—ã

flag = False   # —Ñ–ª–∞–≥ –¥–ª—è –ø–æ–Ω–∏–º–∞–Ω–∏—è, –µ—Å—Ç—å –ª–∏ –ø–æ–∏—Å–∫–æ–≤–æ–π –∑–∞–ø—Ä–æ—Å –≤ —Å—Ç—Ä–æ–∫–∞—Ö –∏–ª–∏ –Ω–µ—Ç

for i in range(n):
    lst.append(input())

n_zaprosov = int(input())

for i in range(n_zaprosov):
    zaprosi.append(input())

# —á–µ—Ä–µ–∑ –¥–≤–æ–π–Ω–æ–π —Ü–∏–∫–ª –ø—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∂–¥—É—é —Å—Ç—Ä–æ–∫—É, –Ω–∞ –≤—Ö–æ–∂–¥–µ–Ω–∏–µ –≤ –Ω–µ–µ –ø–æ–∏—Å–∫–æ–≤—ã—Ö –∑–∞–ø—Ä–æ—Å–æ–≤. –ï—Å–ª–∏ –≤ —Å—Ç—Ä–æ–∫–µ –Ω–µ—Ç —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ–≥–æ –∏–∑ –∏–º–µ—é—â–∏—Ö—Å—è –∑–∞–ø—Ä–æ—Å–æ–≤, —Ç–æ
# –∑–∞–≤–µ—Ä—à–∞–µ–º –≤—Ç—Ä–æ–π (k) —Ü–∏–∫–ª, —Ç.–µ. –ø–µ—Ä–µ—Å—Ç–∞–µ–º –ø—Ä–æ–≤–µ—Ä—è—Ç—å –≤—Ö–æ–∂–¥–µ–Ω–∏—è –æ—Å—Ç–∞–ª—å–Ω—ã—Ö –∑–∞–ø—Ä–æ—Å–æ–≤, –∏ –ø–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç—Ä–æ–∫–µ
for i in range(len(lst)):
    for k in range(len(zaprosi)):
            if zaprosi[k].lower() in lst[i].lower():   # —á–µ—Ä–µ–∑ .lower() —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Ä–µ–≥–∏—Å—Ç—Ä—ã, —á—Ç–æ–±—ã –Ω–µ –æ–±—Ä–∞—â–∞—Ç—å –≤–Ω–∏–º–∞–Ω–∏–µ –Ω–∞ —Ä–µ–≥–∏—Å—Ç—Ä
                flag = True
            else:
                flag = False
                break

# –≤—ã–≤–æ–¥–∏–º —Å—Ç—Ä–æ–∫—É —Ç–æ–ª—å–∫–æ –≤ —Ç–æ–º —Å–ª—É—á–∞–µ, –µ—Å–ª–∏ –≤—Å–µ –ø–æ–∏—Å–∫–æ–≤—ã–µ –∑–∞–ø—Ä–æ—Å—ã –µ—Å—Ç—å –≤ —Å—Ç—Ä–æ–∫–µ
    if flag == True:
        print(lst[i])
