# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def is_palindrome(text):

    new_text = ''
    # –∑–∞–ø–æ–ª–Ω—è–µ–º –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫ —Ç–æ–ª—å–∫–æ –±—É–∫–≤–∞–º–∏ –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ
    for i in range(len(text)):
        if text[i] in alfavit_en or text[i] in alfavit_rus:
            new_text += text[i].lower()
    # –µ—Å–ª–∏ –∏–Ω–≤–µ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ —Ä–∞–≤–Ω–∞ –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ, —Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º True
    if new_text == new_text[::-1]:
        return True
    else:
        return False

